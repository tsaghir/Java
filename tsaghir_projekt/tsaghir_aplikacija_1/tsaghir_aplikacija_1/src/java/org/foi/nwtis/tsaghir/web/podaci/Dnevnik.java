package org.foi.nwtis.tsaghir.web.podaci;

import java.sql.Timestamp;

/**
 * Klasa koja služi za zabilježavanje podataka u dnevnik rada
 * @author tsaghir
 */
public class Dnevnik {
    private int id;
    private String korisnik;
    private String ipadresa;
    private Timestamp vrijeme;
    private long trajanje;
    private int status;
    private String komanda;

    public Dnevnik(String korisnik, String ipadresa, Timestamp vrijeme, long trajanje, int status, String komanda) {
        this.korisnik = korisnik;
        this.ipadresa = ipadresa;
        this.vrijeme = vrijeme;
        this.trajanje = trajanje;
        this.status = status;
        this.komanda = komanda;
    }

    public Dnevnik() {
    }

    

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getKorisnik() {
        return korisnik;
    }

    public void setKorisnik(String korisnik) {
        this.korisnik = korisnik;
    }

    public String getIpadresa() {
        return ipadresa;
    }

    public void setIpadresa(String ipadresa) {
        this.ipadresa = ipadresa;
    }

    public Timestamp getVrijeme() {
        return vrijeme;
    }

    public void setVrijeme(Timestamp vrijeme) {
        this.vrijeme = vrijeme;
    }

    public long getTrajanje() {
        return trajanje;
    }

    public void setTrajanje(long trajanje) {
        this.trajanje = trajanje;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getKomanda() {
        return komanda;
    }

    public void setKomanda(String komanda) {
        this.komanda = komanda;
    }
    
    
}
